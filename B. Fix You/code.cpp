template <typename T>
void input(vector<T> &arr, int n)
{
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
}

void solve()
{
    int n, m;
    cin >> n >> m;
    vector<vector<char>> grid(n, vector<char>(m));
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> grid[i][j];
        }
    }
    int cnt = 0;
    for (int i = 0; i < n - 1; i++)
    {
        if (grid[i][m - 1] != 'D') // if last column is not D change it to D expect the bottommost cell
            cnt++;
    }
    for (int j = 0; j < m - 1; j++)
    {
        if (grid[n - 1][j] != 'R') // if last column is not R change it to R expect the bottommost row cell
            cnt++;
    }
    cout << cnt << "\n";
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}